def tax_chart(tax_amount):
    """
    a helper method that converts a tax amount to the appropriate tax chart values
    :param tax_amount: int - the amount a country taxed for
    :return: list[int, int] - the amount paid to the owner and the amount paid total to the country
    """
    if tax_amount < 6:
        return 0, 0
    elif tax_amount < 8:
        return 1, 1
    elif tax_amount < 10:
        return 1, 2
    elif tax_amount < 11:
        return 2, 3
    elif tax_amount < 12:
        return 2, 4
    elif tax_amount < 13:
        return 3, 5
    elif tax_amount < 14:
        return 3, 6
    elif tax_amount < 15:
        return 4, 7
    elif tax_amount < 16:
        return 4, 8
    elif tax_amount < 18:
        return 5, 9
    else:
        return 5, 10


# bonds are the cost of the bond
def buy_bond(player, country, bond_to_buy, bond_to_trade=None):
    """
    executes a purchase of a bond by a player.
    :param player: Player - the player buying the bond
    :param country: Country - the country associated with the bond
    :param bond_to_buy: int - the cost (bigger number) of the bond to buy
    :param bond_to_trade: int - the cost (bigger number) of the bond being traded in (Nullable)
    :return: boolean - False if operation fails due to insufficient funds
    """
    # Find bonds
    b_t_b = None
    b_t_t = None
    for bond in country.get_bonds():
        if bond.get_cost() == bond_to_buy:
            b_t_b = bond
    if bond_to_trade is not None:
        for bond in player.get_bonds():
            if bond.get_cost() == bond_to_trade:
                b_t_t = bond
        if player.get_money() < bond_to_buy - bond_to_trade or b_t_b is None or b_t_t is None:
            return False
        else:
            player.remove_money(bond_to_buy - bond_to_trade)
            country.add_money(bond_to_buy - bond_to_trade)

            player.swap_bonds(b_t_b, b_t_t)
            country.reclaim_bond(b_t_t)
            country.sell_bond(b_t_b, player)
    else:
        if player.get_money() < bond_to_buy or b_t_b is None:
            return False
        else:
            player.remove_money(bond_to_buy)
            country.add_money(bond_to_buy)

            player.add_bond(b_t_b)
            country.sell_bond(b_t_b, player)


def power_chart(power_value):
    """
    converts a power_value to the appropriate place on the power chart
    :param power_value: int - the power value of a country
    :return: int - the converted power value
    """
    return math.ceil(power_value / 5)


